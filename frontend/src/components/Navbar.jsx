import React from 'react';
import { useNavigate } from 'react-router-dom';
import Dock from './Dock';

const HomeIcon = () => <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6"><path strokeLinecap="round" strokeLinejoin="round" d="m2.25 12 8.954-8.955c.44-.439 1.152-.439 1.591 0L21.75 12M4.5 9.75v10.125c0 .621.504 1.125 1.125 1.125H9.75v-4.875c0-.621.504-1.125 1.125-1.125h2.25c.621 0 1.125.504 1.125 1.125V21h4.125c.621 0 1.125-.504 1.125-1.125V9.75M8.25 21h8.25" /></svg>;
const BrowseIcon = () => <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6"><path strokeLinecap="round" strokeLinejoin="round" d="m21 21-5.197-5.197m0 0A7.5 7.5 0 1 0 5.196 5.196a7.5 7.5 0 0 0 10.607 10.607Z" /></svg>;
const AddIcon = () => <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6"><path strokeLinecap="round" strokeLinejoin="round" d="M12 9v6m3-3H9m12 0a9 9 0 1 1-18 0 9 9 0 0 1 18 0Z" /></svg>;
const AboutIcon = () => <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6"><path strokeLinecap="round" strokeLinejoin="round" d="m11.25 11.25.041-.02a.75.75 0 0 1 1.063.852l-.708 2.836a.75.75 0 0 0 1.063.853l.041-.021M21 12a9 9 0 1 1-18 0 9 9 0 0 1 18 0Zm-9-3.75h.008v.008H12V8.25Z" /></svg>;

const Navbar = ({ activeHref }) => {
    const navigate = useNavigate();

    const navItems = [
        { href: '/', label: 'Home', icon: <HomeIcon /> },
        { href: '/browse', label: 'Browse', icon: <BrowseIcon /> },
        { href: '/add', label: 'Add', icon: <AddIcon /> },
        { href: '/about', label: 'About', icon: <AboutIcon /> },
    ];

    const activeIndex = navItems.findIndex(item => item.href === activeHref);

    // The unnecessary <div> wrapper has been removed.
    return (
        <Dock
            className="!bottom-auto !top-4 text-gray-300 hover:text-white transition-colors"
            activeIndex={activeIndex}
            items={navItems.map((item) => ({
                icon: item.icon,
                label: item.label,
                onClick: () => navigate(item.href)
            }))}
        />
    );
};

export default Navbar;